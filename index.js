const cardContainer = document.querySelector('.educational-materials');

function renderCards() {
  pdfData.forEach(card => {
    const div = document.createElement("div");
    div.className = "pdf-card";
    div.innerHTML = `
      <div class="card-header">
        <span class="pages">${card.pages} páginas</span>
      </div>
      <div class="card-image">
        <img src="${card.thumbnail}" alt="${card.title}" />
      </div>
      <div class="card-body">
        <h3>${card.title}</h3>
        <p>${card.description}</p>
      </div>
      <div class="card-footer">
        <button onclick="openModal(${card.id})">Ver mais</button>
      </div>
    `;
    cardContainer.appendChild(div);
  })
}

document.addEventListener("DOMContentLoaded", () => {
  renderCards();
});

let currentSlides = [];
let currentSlide = 0;

function openModal(id) {
  const card = pdfData.find(c => c.id === id);

  if(!card) return;

  document.getElementById("modal-title").textContent = card.title;
  currentSlides = card.slides;
  currentSlide = 0;
  updateSlider();
  document.getElementById("modal").classList.remove("modal-hidden");
}

function closeModal() {
  const modal = document.getElementById("modal")
  modal.classList.add("modal-hidden");
}

function updateSlider() {
  const img = document.getElementById("slide-image");
  img.src = currentSlides[currentSlide];
  const counterEl = document.getElementById("slide-counter");

 if(counterEl) {
  counterEl.textContent = `${currentSlide + 1} / ${currentSlides.length}`;
 } 

 const thumbContainer = document.getElementById("thumbnail-row");
 thumbContainer.innerHTML = "";

 currentSlides.forEach((src, index) => {
  const thumb = document.createElement("img");
  thumb.src = src;
  
  if(index === currentSlide) thumb.classList.add('active');

  thumb.onclick = () => {
    currentSlide = index;
    updateSlider();
  }

  thumbContainer.appendChild(thumb);
 })
}

function nextSlide() {
  if(currentSlide < currentSlides.length - 1){
    currentSlide++;
    updateSlider();
  }
}

function prevSlide() {
  if(currentSlide > 0) {
    currentSlide--;
    updateSlider();
  }
}

